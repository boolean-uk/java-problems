package com.booleanuk.easy;

/**
 * Find the product of all positive integers less than or equal to any given number. This is known as the factorial (denoted by !)
 * E.g.
 * Given the number 5, the factorial is 5 x 4 x 3 x 2 x 1 = 120
 */
public class Factorial {
    public int solution(int input) {
        int multi=1;
        for (int i = 2; i <= input; i++) {
            multi*=i;
        }
        return multi;
    }
}
